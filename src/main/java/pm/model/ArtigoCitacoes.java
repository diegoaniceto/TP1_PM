package pm.model;

public class ArtigoCitacoes {
	private String artigoCitado;
	private String artigoCitador;
	
	public ArtigoCitacoes(String artigoCitado, String artigoCitador) {
		this.artigoCitado = artigoCitado;
		this.artigoCitador = artigoCitador;
	}

	public String getArtigoCitado() {
		return artigoCitado;
	}

	public String getArtigoCitador() {
		return artigoCitador;
	}
	
	
}
